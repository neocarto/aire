<a name=""></a>
<dt>Anamorphose</dt>
<dd>
  Représentation des surfaces (pays, régions ou maille quelconque) en fonction de quantités qui lui sont rattachées. Le maillage est déformé selon l’importance du phénomène représenté.
</dd>

<a name=""></a>
<dt>Discrétisation</dt>
<dd>
  La discrétisation est une opération permettant le découpage en classes d’une série statistique, quantitative ou qualitative. Cette simplification de l’information permet de regrouper dans une même classe les objets géographiques qui se ressemblent le plus et qui possèdent les mêmes caractéristiques. La discrétisation doit conserver les caractéristiques essentielles des données, perdre le moins d’information possible et respecter les règles liées à la perception visuelle.
</dd>

<a name=""></a>
<dt>Carte thématique</dt>
<dd>
  Ce type de carte permet de représenter, sur un fond repère topographique, hydrographique, chorographique ou géographique, des phénomènes localisables dans l’espace sans être directement perceptible à la surface du sol. Elle décrit l’espace en localisant la nature et l’importance d’un phénomène. Il existe deux grands types de cartes thématiques :
  <ul>
    <li>Les quantitatives (ou cartes statistiques) qui permettent l’analyse des données chiffrées, donnant une valeur (absolue ou relative) à un espace.</li>
    <li>Les qualitatives ou conceptuelles qui représentent graphiquement des idées ou des raisonnements complexes.</li>
  </ul>
</dd>

<a name=""></a>
<dt>Echelle</dt>
<dd>
  L’échelle est le rapport entre une distance mesurée sur la carte et la distance réelle mesurée sur le terrain.
</dd>

<a name=""></a>
<dt>Figuration</dt>
<dd>
  Méthode de représentation graphique. Présentation d’une donnée d’une image ou d’un objet par un élément graphique symbolique ou géométrique. Construction ou image graphique représentant une donnée, une image ou un objet. Synonyme de figuré.
</dd>

<a name=""></a>
<dt>Fonction gaussienne</dt>
<dd>
  Il s’agit d’une fonction en exponentielle de l’opposé du carré de l’abscisse. Elle a une forme caractéristique de courbe en cloche et l’exemple le plus connu est la densité de probabilité de la loi dite de Gauss (ou loi normale). Cette fonction est utilisée dans AIRE pour le calcul des cartes de potentiels. Ainsi, pour chaque point de l’espace, la valeur du potentiel calculé prend fortement en compte les points proches de lui, puis, de façon décroissante les autres points à fur et à mesure de l’éloignement. Le paramètre de portée, définit l’aplatissement de la courbe.
</dd>

<a name=""></a>
<dt>Généralisation</dt>
<dd>
  Opération de simplification des tracés ou des phénomènes représentés sur une carte. On peut distinguer la généralisation structurale qui conserve le mode de représentation et d’implantation d’un phénomène tout en schématisant sa structure originale et la généralisation conceptuelle qui transforme le mode de représentation d’un phénomène pour répondre à un nouveau concept et/ou à un changement de niveau d’observation.
</dd>

<a name=""></a>
<dt>GREAT</dt>
<dd>
   Fond Généralisé des Régions Européennes pour l’ Aménagement du Territoire.<br/>
   Ce fond de carte, adapté pour la réalisation de cartes thématiques à l’échelle des régions européennes a été réalisé par UMS RIATE. Ce fond de carte est disponible à plusieurs dates (1980, 1988, 1995, 1999, 2003 et 2006) pour rendre compte des changements de maillages des régions en Europe. Il est libre de droit pour un usage non commercial.
</dd>

<a name=""></a>
<dt>Implantation</dt>
<dd>
  Pour localiser sur un plan un lieu déterminé en "x" et en "y", le cartographe dessine à l'endroit convenable une tâche visible. Sur la feuille de papier la tache occupe une superficie plus ou moins étendue et peut prendre la forme de trois figures élémentaires: le point, la ligne et la zone. C'est ce que l'on appelle le mode d'implantation de la tâche sur le plan. L'implantation est donc la localisation des éléments graphiques dans le plan de la carte.
</dd>

<a name=""></a>
<dt>Langage SVG (Scalable Vector Graphics)</dt>
<dd>
  Format graphique qui repose sur le langage XML (eXtensible Markup Language) dont le but est la description d’objets graphiques en 2D. Le SVG permet de gérer trois types d’objets graphiques, les formes vectorielles (courbes, lignes, polygones, etc.), les textes et les images raster. Une image SVG n’est pas basée sur un ensemble de pixels mais sur des figures géométriques, dont on précise la texture et le comportement. Le SVG permet de mettre en place du contenu dynamique et interactif, ainsi que de modifier les vecteurs en fonction du temps. AIRE offre la possibilité de sauvegarder les cartes au format SVG.
</dd>

