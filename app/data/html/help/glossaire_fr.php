<a name=""></a>
<dt>NUTS</dt>
<dd>
  La NUTS (nomenclature des unités territoriales statistiques) définit, pour les 27 États membres de l’Union européenne, une nomenclature hiérarchique qui subdivise chaque État en unités statistiques emboitées. Alors que celle-ci a été crée dans les années 1970, ce n’est qu’en 2003 qu’a été adopté un règlement du Parlement européen et du Conseil sur cette nomenclature. La nomenclature NUTS actuelle (NUTS 2006), valable à compter du 1<sup>er</sup> janvier 2008, subdivise le territoire économique de l’Union européenne en 97 régions de niveau NUTS 1, 274 régions de niveau NUTS 2 et 1 303 régions de niveau NUTS 3.
</dd>

<a name=""></a>
<dt>Projection</dt>
<dd>
  Procédé géométrique de correspondance entre les points de la surface du globe et ceux de la surface plane de la terre. Les projections possibles sont très nombreuses et dépendent de la fonction et de l’objectif de la carte. Pour la représentation de l’Europe, on utilise traditionnellement la projection ETRS-LAEA.
</dd>

<a name=""></a>
<dt>Qualitatif</dt>
<dd>
  Caractéristique d’ une donnée qui traduit non des quantités mais des états distincts, des catégories différentes. On distingue les données qualitatives dont l’ordre a un sens (qualitative ordinale) et celles pour lesquelles il n’en a pas (qualitative nominale).
</dd>

<a name=""></a>
<dt>Rapport ou Relatif</dt>
<dd>
  Caractéristique d’ une donnée statistique permettant d’établir une relation ordonnée entre les objets géographiques. Les données relatives sont des données sans dimension comme les pourcentages, les taux et les indices.
</dd>

<a name=""></a>
<dt>Sémiologie graphique</dt>
<dd>
  Science générale des signes appliquée à la cartographie. Langage cartographique permettant l’utilisation de signes graphiques en relation avec les informations qu’ils expriment.
</dd>

<a name=""></a>
<dt>Stock ou Quantitatif</dt>
<dd>
  Caractéristique d’une donnée dont les valeurs expriment des quantités issues de mesure ou de dénombrement. On distingue les quantités brutes ou absolues (de stock) et les quantités relatives. On différencie également celles pour lesquelles la valeur zéro signifie absence de et celles pour lesquelles elle est arbitraire (la température ou l’altitude).
</dd>

<a name=""></a>
<dt>Types de représentation</dt>
<dd>
  Langage, système et moyens graphique de représentation cartographique.
</dd>

<a name=""></a>
<dt>Variables visuelles</dt>
<dd>
  Les variables visuelles sont les moyens graphiques qui permettent de différencier les données représentées dans le plan de la carte. Chaque variable visuelles possède ses propres caractéristiques et sera plus ou moins bien adaptée à la représentation des caractères qualitatifs ou quantitatifs. Il existe deux grands types de variables visuelles: les variables naturelles ou physiologiques (la forme, la taille, la couleur, la valeur, l'orientation) et les variables construites ou élaborées (le grain, la texture/structure). Elles sont perçues comme sélectives (orientation, valeur, couleur, taille, grain), ordonnées (valeur, grain, taille) ou quantitative (taille).
</dd>

