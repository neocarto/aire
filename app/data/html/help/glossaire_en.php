<dl>

<a name=""></a>
<dt>Anamorphosis or cartogram</dt>
<dd>
An abstracted and simplified map which the base is not true to geographic scale. In the most common forms, the areas of features are drawn according to a value, such as population.
</dd>

<a name=""></a>
<dt>Classification</dt>
<dd>
Placing data into groups that have similar characteristics or values. Categorizing the values for a choropleth map. Unless the enumeration areas all the same size, densities, ratios, percentages, or other derived value should be used. This operation is essential in order to simplify the image and improve the possibilities for a communication of the information. A condition is that differences within classes have been minimized and differences between classes maximized. The number of classes must be limited by the number that the human eye can recognize. A variety of techniques (mathematical or graphical) can be used to choose an appropriate series of categories.
</dd>

<a name=""></a>
<dt>Dot map</dt>
<dd>
A representation of geographic phenomena on which dots represent a specified number of the phenomenon being mapped.
</dd>

<a name=""></a>
<dt>European grid</dt>
<dd>
  Cf. <a href="#NUTS">NUTS</a>
</dd>

<a name=""></a>
<dt>Gaussian function</dt>
<dd>
In one dimension, the Gaussian function is the probability density function of the normal distribution, sometimes also called the frequency curve. In two dimensions, the circular Gaussian function is the distribution function for uncorrelated variates X and Y having a bivariate normal distribution and equal standard deviation. This function is used for AIRE for the smoothing maps. At any point in space, the calculated potential value takes in account all the others points near it, and less and less with distance in a gradual manner.
</dd>

<a name=""></a>
<dt>Generalization</dt>
<dd>
A basic cartographic procedure that reduces the amount of information presented in order to crate clearer communication. Because maps are drawn smaller than reality, they must be generalized. The processes involved in generalization are selection, simplification, classification, and symbolization.
</dd>

<a name=""></a>
<dt>GREAT</dt>
<dd>
  Generalized Representation for European Areas and Territories<br/>
  GREAT is a base map adapted to thematic mapping of European regions. It has been designed by UMS RIATE. It is available for several dates according to territorial division’ s evolution in Europe (1980, 1988, 1995, 1999, 2003 and 2006). Free for non commercial use.
</dd>

<a name=""></a>
<dt>Interval measurement</dt>
<dd>
Provides information about differences in value between ranks.
</dd>

<a name=""></a>
<dt>Linear symbol</dt>
<dd>
A line used to represent geographic phenomena that are linear in nature.
</dd>

<a name=""></a>
<dt>Nominal measurement</dt>
<dd>
Distinguishes between things or classifies them on the basis of their nature without any indication of quantity.
</dd>

<a name=""></a>
<dt>NUTS</dt>
<dd>
The NUTS nomenclature (Nomenclature of Territorial Units for Statistics) is a geocode standard for referencing the subdivisions of countries for statistical purposes. The standard is developed and regulated by the European Union, and thus only covers the member states of the EU in detail. The Nomenclature of Territorial Units for Statistics is instrumental in European Union's Structural Fund delivery mechanisms.<br/>
The actual NUTS, named, NUTS 2006, is operational from 2008 January 1rst. They subdivide the European Union in 97 regions at NUTS1 level, 274 regions at NUTS2 level and 1303 regions at NUTS3 level.
</dd>

<a name=""></a>
<dt>Ordinal measurement</dt>
<dd>
Classifies and rank data without specifying numerical values.
</dd>

<a name=""></a>
<dt>Projection</dt>
<dd>
A systematic arrangement of all r a part of the earth’ s grid on a plane. The projection of Europe is usually the ETRS-LAEA projection.
</dd>

<a name=""></a>
<dt>Ratio measurement</dt>
<dd>
Classifies and gives differences between values using a scale that starts at absolute zero.
</dd>

<a name=""></a>
<dt>Scale</dt>
<dd>
The ratio of the distances on a map, to the actual distances they represent.
</dd>

<a name=""></a>
<dt>Semiotics</dt>
<dd>
The theory of signs and symbols
</dd>

<a name=""></a>
<dt>Smoothing</dt>
<dd>
A generalization process that eliminates some details of complex linear features.
</dd>

<a name=""></a>
<dt>SVG (Scalable Vector Graphics) language</dt>
<dd>
Graphic format that use the XLM language (eXtensible Markup Language) to describe 2D graphic objects. The SVG format allows managing 3 types of graphic elements: vector format elements (curves, lines, polygons, etc.), texts, and rater images. A SVG image is not composed with pixels but with geometric images. AIRE offers the possibility to save maps in SVG format
</dd>

<a name=""></a>
<dt>Symbolization</dt>
<dd>
The process of designing or selecting symbols for a map. Graphic representation method. Graphical construction representing data, image, or phenomenon.
</dd>

<a name=""></a>
<dt>Thematic map</dt>
<dd>
Thematic maps represent the distribution of one particular phenomenon. The base map used for a thematic map is often a topographic map or the most relevant elements from topographic maps. The base map serves only as a framework to locate the phenomenon being mapped.
</dd>

<a name=""></a>
<dt>Visual variables of symbols</dt>
<dd>
Graphic characteristics symbols. These include size, shape, tonal value, hue, orientation and pattern.
</dd>

<a name=""></a>
<dt>Volume phenomena</dt>
<dd>
Geographical phenomena that extend over areas but can be conceived of as having a third dimension. The third dimension is a value or quantity.
</dd>

<a name=""></a>
<dt>Z values</dt>
<dd>
On statistical maps, the z values are the values associated with points or areas.
</dd>

